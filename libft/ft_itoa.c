#include "libft.h"

char		*ft_itoa(int n)
{
	int		l;
	long	p;
	char	*r;

	l = (n <= 0) ? 1 : 0;
	p = n;
	while (p)
	{
		l++;
		p /= 10;
	}
	if (!(r = (char*)malloc(l + 1)))
		return (r);
	r[l] = 0;
	r[0] = (n < 0) ? '-' : '0';
	p = (n > 0) ? n : (-(long)n);
	while (p)
	{
		r[--l] = p % 10 + '0';
		p /= 10;
	}
	return (r);
}
